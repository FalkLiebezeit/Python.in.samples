#  simulates stack using a list 
   
stk = [] 
  
# push using append()method of list 
 
stk.append(10)
print('\nStack now contains:', stk)
stk.append(20)
print('\nStack now contains:', stk)
stk.append(30)
print('\nStack now contains:', stk)
stk.append(35)
print('\nStack now contains:', stk)

 
# pop from the stack    

print('the item popped is: ', stk.pop())
print('\nStack now contains:', stk)
print('the item popped is: ', stk.pop())
print('\nStack now contains:', stk)
print('the item popped is: ', stk.pop()) 
print('\nStack now contains:', stk)
