# Unary operator example program in Python

# Define a variable
x = 10

# Unary positive operator (+)
result_positive = +x
print("Unary positive operator (+):", result_positive)  # Output: 10

# Unary negative operator (-)
result_negative = -x
print("Unary negative operator (-):", result_negative)  # Output: -10

# Unary bitwise NOT operator (~)
result_bitwise_not = ~x
print("Unary bitwise NOT operator (~):", result_bitwise_not)  # Output: -11 (complement of x in two's complement representation)

# Unary logical NOT operator (not)
y = True
result_logical_not = not y
print("Unary logical NOT operator (not):", result_logical_not)  # Output: False

