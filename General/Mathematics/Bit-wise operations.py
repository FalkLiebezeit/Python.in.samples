# Bit-wise operations

a=45
b=89
print('binary of a=', bin(a)) # prints the binary value of a
print('binary of b=', bin(b)) # prints the binary value of x
print(('a OR b=', a|b)) #prints a OR b
print(('a AND b=', a&b)) # prints a AND b
print(('complement of a =',~a)) # prints complement of a
print(('shifting a by 4 bits to the left=', a<<4))
print(('shifting a by 2 bits to the right=', a>>2))
print(('exclusive OR of a b =', a^b))
